    {
        
        $form = new Student();
    
        $form->first_name=request('name');
        $form->last_name = request ('lname');
        $form->register_no= request('registration');

        $form->address_door_no=request('door');
        $form->address_sub_locality_1=request('local');
       

        $form->save();
